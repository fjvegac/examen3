    void Registrar(){
        String nombre = txtNombre.getText();
        String tipo = (String)cbTipo.getSelectedItem();
        String insertar = "INSERT INTO PRODUCTO(ID_PRODUCTO, NOMBRE, PRECIO, TIPO) VALUES (?, ?, ?, ?)";
        try{
            ps = cin.prepareCall(insertar);
            ps.setString(1, txtID.getText());
            ps.setString(2, nombre);
            ps.setString(3, txtPrecio.getText());
            ps.setString(4, tipo);
            
            int registro = ps.executeUpdate();
            if (registro >0){
                JOptionPane.showMessageDialog(this, "Registro completado.", "Bien",JOptionPane.QUESTION_MESSAGE);
            }else{
                JOptionPane.showMessageDialog(this, "Registro no completado.", "Mal",JOptionPane.ERROR_MESSAGE);
            }
        }catch(Exception e){
            
        }
    }
